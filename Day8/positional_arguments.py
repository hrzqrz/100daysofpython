# positional argument
def greet(name, location):
    print(f'Hello {name}')
    print(f'{name}: i am form {location}')
    

#Positional argument function recal
greet('Safa', 'Tehran')

#keword argument

def greet2(name, location):
    print(f'Hello, {name}')
    print(f'Hello to your location: {location}')
 
# keyword argument function recall   
greet2(location='rasht', name='Safa')
    